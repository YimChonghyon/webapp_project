
function home_button() {
  var ho = document.getElementById("home");
  var sl = document.getElementById("slides");
    ho.style.display = 'block';
    sl.style.display = 'none';
}

function slide_button() {
  var ho = document.getElementById("home");
  var sl = document.getElementById("slides");
    ho.style.display = 'none';
    sl.style.display = 'block';
}
